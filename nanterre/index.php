            <h2>Wikipédia fête ses dix ans !</h2>

            <p>À l'occasion du dixième anniversaire de Wikipédia, Wikimédia France et la Ville de Nanterre organisent, le vendredi 29 avril à partir de 20h00, dans le cadre du festival "Déclics !", une soirée spéciale.</p>

            <p>Le programme de cette soirée inclut notamment :</p>

            <ul>
                <li>Une conférence-débat sur les réalisations et l'avenir de Wikipédia. Avec la participation de :
                <ul>
                    <li>Mélanie Dulong, responsable juridique de <a href="http://fr.creativecommons.org" title="Creative Commons France">Creative Commons France</a> ;</li>
                    <li>Julien Fayolle, ancien vice-président de l'<a href="http://www.wikimedia.fr" title="Wikimédia France">association Wikimédia France</a> ;</li>
                    <li>Laurent Gaveau, responsable du service Nouveaux médias du <a href="http://www.chateauversailles.fr" title="Site du château de Versailles">Château de Versailles</a> ;</li>
                    <li>Marcos Giadas, maître de conférences en information et communication à l'<a href="http://www.u-paris10.fr/" title="Université Paris X">université Paris Ouest Nanterre La Défense</a> ;</li>
                    <li>Gaël Musquet, responsable du comité de données à l'OpenSource Géospatial francophone et contributeur d'<a href="http://www.openstreetmap.fr" title="OpenStreetMap">OpenStreetMap</a>.</li>
                </ul>
                À la fin de chaque partie de la conférence, vous pourrez poser vos questions aux intervenants ;</li>

                <li>Une projection d'extraits du film "Truth in Numbers ? Everything according to Wikipedia", sorti aux États-Unis en 2010 ;</li>
                <li>La première représentation de la "WikiChanson", écrite à l'occasion de cet anniversaire ;</li>
                <li>Un pot amical entre participants.</li>
            </ul>

            <p>Cette soirée sera précédée (vendredi 29 avril à partir de 17h30) par un atelier d'initiation à la rédaction d'articles sur Wikipédia, et sera suivie (samedi 30 avril à partir de 14h30) par une promenade en équipes dans les rues de Nanterre, destinée à enrichir Wikimédia Commons (médiathèque fonctionnant comme Wikipédia) de nombreuses photographies de la ville et à appréhender ensemble le droit d'auteur et le droit à l'image.</p>

